CREATE TABLE [Beschwörbares] (
	[GegnerBaseGUID] uniqueidentifier NOT NULL PRIMARY KEY, 
	[Beschwörung] int NOT NULL, 
	[Kontrolle] int, 
	[Beschwörungskosten] int, 
	FOREIGN KEY ([GegnerBaseGUID])
		REFERENCES [GegnerBase] ([GegnerBaseGUID])
		ON UPDATE CASCADE ON DELETE CASCADE
)
GO

CREATE TABLE [Dämon] (
	[GegnerBaseGUID] uniqueidentifier NOT NULL PRIMARY KEY, 
	[Hörner] int, 
	FOREIGN KEY ([GegnerBaseGUID])
		REFERENCES [Beschwörbares] ([GegnerBaseGUID])
		ON UPDATE CASCADE ON DELETE CASCADE
)
GO

CREATE TABLE [Dämon_Domäne] (
	[GegnerBaseGUID] uniqueidentifier NOT NULL, 
	[Domäne] nvarchar(100) NOT NULL,
	CONSTRAINT [PK_Dämon_Domäne] PRIMARY KEY ([GegnerBaseGUID], [Domäne]), 
	FOREIGN KEY ([GegnerBaseGUID])
		REFERENCES [Dämon] ([GegnerBaseGUID])
		ON UPDATE CASCADE ON DELETE CASCADE
)
GO

CREATE TABLE [Elementarwesen] (
	[GegnerBaseGUID] uniqueidentifier NOT NULL PRIMARY KEY, 
	[Element] nvarchar(100), 
	[Wesen] nvarchar(100), 
	FOREIGN KEY ([GegnerBaseGUID])
		REFERENCES [Beschwörbares] ([GegnerBaseGUID])
		ON UPDATE CASCADE ON DELETE CASCADE
)
GO

